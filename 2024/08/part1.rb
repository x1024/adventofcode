def check(data)
  seen = Set.new
  data.each do |(pos, c)|
    next unless c.match?(/[a-zA-Z0-9]/)

    data.each do |(pos2, c2)|
      next unless c == c2
      next if pos == pos2

      distance = pos2 - pos
      # next unless distance.real.positive?

      antinode1 = pos2 + distance
      antinode2 = pos - distance
      p [c, c2, pos, pos2, distance, antinode1, antinode2]
      if data.key?(antinode1)
        seen.add(antinode1)
        p 'antinode1'
      end
      if data.key?(antinode2)
        seen.add(antinode2)
        p 'antinode2'
      end
    end
  end
  seen.size
end

def main(input)
  data = input.split("\n").map.with_index do |line, x|
    line.each_char.map.with_index { |c, y| [x + y.i, c] }
  end.flatten(1).to_h

  p data
  result = check(data)

  # p lines
  # p data

  puts 'Result:', result
end

